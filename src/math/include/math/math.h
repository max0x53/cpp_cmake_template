namespace math {

/**
 * @brief Adds two numbers together
 *
 * WARNING: Integer overflows are not handled, undefined behaviour may occur here
 * 
 * @param a an integer to add
 * @param b an integer to add
 * @return int the result of the addition of a and b 
 */
int add(int a, int b);

/**
 * @brief Multiplies two numbers together
 *
 * WARNING: Integer overflows are not handled, undefined behaviour may occur here
 * 
 * @param a an integer to multiple
 * @param b an integer to multiple
 * @return int the result of the multiplication of a and b 
 */
int multiply(int a, int b);

}